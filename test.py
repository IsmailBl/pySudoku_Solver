operateur = input("Please Enter your operator : ")
if (operateur == "+"):
    a = float(input ("Enter a : "))
    b = float(input ("Enter b : "))
    print("The result is : ",(a+b))
elif (operateur == "//"):
    a = float(input ("Enter a : "))
    b = float(input ("Enter b : "))
    print("The result is : ",(a//b))

elif (operateur == "-"):
    a = float(input ("Enter a : "))
    b = float(input ("Enter b : "))
    print("The result is : ",(a-b))

elif (operateur == "*"):
    a = float(input ("Enter a : "))
    b = float(input ("Enter b : "))
    print("The result is : ",(a*b))